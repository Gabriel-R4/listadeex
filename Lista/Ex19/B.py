
# ```
# \(\d{3}\) \d{3}-\d{4}
# ```

# Esta expressão regular busca por sequências específicas dentro do texto:

# - `\(\d{3}\)`: Procura por um padrão onde há um parêntese de abertura seguido por exatamente três dígitos e um parêntese de fechamento.
# - `\d{3}`: Procura por mais três dígitos.
# - `-`: Procura pelo caractere de traço.
# - `\d{4}`: Procura por quatro dígitos consecutivos.
